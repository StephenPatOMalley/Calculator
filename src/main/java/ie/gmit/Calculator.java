package ie.gmit;

public class Calculator {

    public int Add(int firstNum, int secondNum){

        int addedNum = 0;
        addedNum = firstNum + secondNum;

        return addedNum;
    }

    public int SubTract(int firstNum, int secondNum){
        int subNum = 0;
        subNum = firstNum - secondNum;

        return subNum;
    }

}
